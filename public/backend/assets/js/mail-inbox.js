(function($)
{
    "use strict";
    $(document).ready(function () {

        //---------------------------------------------------------------------------------------------
        // - MAIL NAV TOGGLE --------------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------

        $('#mail-nav-toggle').on('click', function(){
            $(".box-mail .nav").slideToggle();
        });

        //---------------------------------------------------------------------------------------------
        // - MAIL MESSAGE FAVORIT ---------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------

        $(".mail-star").on('click', function () {
            $(this).find("i").text(($(this).find("i").text()=== "star"? "star_outline":"star"));
        });

        //---------------------------------------------------------------------------------------------
        // - MAIL CHECK ALL MESSAGES ------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------

        $("#mail_select_all").on('click', function () {
            var checkBoxes = $(".custom-control-input");
            checkBoxes.prop("checked", !checkBoxes.prop("checked"));
        });


    });
})(jQuery);


